    public void callSelectedRow(JTable table){ // 선택한 row의 값을 새로운 창의 textFeild에 넣어서 보여준다.
        setTable(table);
        for (int i = 0; i < colCount; i++) {            
            System.out.print("view: " + table.getModel().getValueAt(row, i )+"\n");           
            String data = (String) table.getModel().getValueAt(row, i );
            switch(i){
               case 0:
                   originalStoreNumber = data;
                   jTextField1.setText(data);
                   break;
               case 1:
                    jTextField2.setText(data);
                    break;
               case 2:
                    jTextField3.setText(data);
                    break;
               case 3:
                    jTextField4.setText(data);
                    break;                           
            }                         
        }     
    }

