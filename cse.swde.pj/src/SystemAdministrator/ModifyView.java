        // 수정 완료 버튼을 눌렀을 때 발생하는 이벤트
        // 입력받은 새로운 정보 db에 업데이트 하기         
        String storeNumber = jTextField1.getText();
        String storeName = jTextField2.getText();
        String storeOwner = jTextField3.getText();
        String storeAddress = jTextField4.getText();
        
        String[] modifyData = {storeNumber, storeName, storeOwner, storeAddress};
        System.out.println(storeNumber + storeName + storeOwner + storeAddress);
        StoreListDb db = new StoreListDb();
        event = db.modifyUpdate(modifyData, originalStoreNumber); // db에서 수정하기위해 새로 입력받은 textfield 값을 배열형태로 넘겨줌
        // db 수정이 성공하면 event가 true가 됨
        if(event == true) {
        dispose(); // 수정 gui frame만 종료시킴
        }
        System.out.println("modify");
        SystemAdminView adminView = new SystemAdminView();
        adminView.dispose();
        adminView.setTitle("가맹점 신청 관리"); // gui창 제목 지정  
